def analyze(data):
    mean = np.mean(data)
    std = np.std(data, ddof=1)
    n = len(data)
    ci_low, ci_high = stats.t.interval(
        0.95, n-1, loc=mean, scale=std/np.sqrt(n)
    )
    return mean, std, ci_low, ci_high

